uint8_t Convert233(uint16_t color){
  uint32_t red,green,blue;
  uint8_t out;
  blue = (color&0xF800)>>10; // bits 15-11
  if(blue){
    while((blue&0x01)==0){
      blue = blue>>1;
    }
    while(blue>7){
      blue = blue>>1;
    }
  }
  green = (color&0x07E0)>>5; // bits 10-5
  if(green){
    while((green&0x01)==0){
      green = green>>1;
    }
    while(green>7){
      green = green>>1;
    }
  }
  red = color&0x001F;        // bits 4-0
  if(red){
    while((red&0x01)==0){
      red = red>>1;
    }
    while(red>3){
      red = red>>1;
    }
  }
  out = (red<<6)|     // red is bits 7,6
        (green<<3)|   // green is bits 5,4,3
        (blue);       // blue is bits 2,1,0
  return out;
}
